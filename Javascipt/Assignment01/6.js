
var celsius = 22;


if (celsius < 0) {
    console.log("Freezing Cold");
} 
else if (celsius >= 0 && celsius <= 15) {
    console.log("Very Cold");
} 
else if (celsius >= 16 && celsius <= 25) {
    console.log("Cold");
} 
else if (celsius >= 26 && celsius <= 35) {
    console.log("Warm");
} 
else { 
    console.log("Hot");
}

